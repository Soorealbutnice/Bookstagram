package com.dnb.post.model;

public class HashtagDto extends PostDto {

	private String hashtag_no;
	private String hashtag;
	
	public String getHashtag_no() {
		return hashtag_no;
	}
	public void setHashtag_no(String hashtag_no) {
		this.hashtag_no = hashtag_no;
	}
	public String getHashtag() {
		return hashtag;
	}
	public void setHashtag(String hashtag) {
		this.hashtag = hashtag;
	}
	
	
}
